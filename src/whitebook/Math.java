package whitebook;

public class Math {
	
	public static void main(String[] args) {
		
		int x;
		x = 1;
		
		int y;
		y = 7;
		
		int z;
		z = 195;
		
		System.out.println(x+y+z);
		System.out.println(x*y*z);
		System.out.println(x/y/z);
		
		double x1;
		x1 = 1;
		
		double y1;
		y1 = 2;
		
		double z1;
		z1 = 8; 
		
		System.out.println(x1+y1+z1);
		System.out.println(x1-y1-z1);
		System.out.println(x1*y1*z1);
		System.out.println(x1+y1+z1);
		
		
		
	}

}
